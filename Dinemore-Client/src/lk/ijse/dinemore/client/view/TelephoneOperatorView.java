                Object[] rowData = {
                    order.getOrderID(),
                    order.getQty(),
                    order.getAddedTime(),
                    order.getTakenTime(),
                    order.getChefID(),
                    order.getCustomerName(),
                    order.getCustomerTel()
                };
                dtm2.addRow(rowData);
