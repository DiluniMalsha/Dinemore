    private void btnOrderAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderAddActionPerformed

        try {
            String orderID = txtOrderID.getText();
            int qty = Integer.parseInt(txtQty.getText());
            String custName = txtCustomerName.getText();
            String custTell = txtCustomerTelll.getText();
            String time = lblTPTime.getText();
            String date = lblTPDate.getText();
            String tpID = comboOperatorID.getSelectedItem().toString();
            OrderDTO orderDTO = new OrderDTO(orderID, custName, custTell, date, "Pending", qty, time, "not yet", "not Yet", tpID, "not Yet");
            boolean save = telephoneOperatorController.save(orderDTO);
            if (save) {
                JOptionPane.showMessageDialog(this, "Order Added");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
            clearText();
        } catch (Exception ex) {
            Logger.getLogger(TelephoneOperatorView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnOrderAddActionPerformed

